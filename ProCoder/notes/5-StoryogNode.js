/**
 * ! History of Node.js
 *
 * - Node.js ko 2009 mein Ryan Dahl ne create kiya tha
 *
 * - Us time problem yeh thi:
 *   Traditional web servers (like Apache) synchronous hote the
 *   → Ek request complete hone tak dusri wait karti thi (slow performance)
 *
 * - Ryan Dahl ne solution diya:
 *   → Non-blocking, event-driven architecture
 *   → Multiple requests ko efficiently handle karna
 *
 *
 * ! Key Milestones
 *
 * - 2009:
 *   → Node.js launch hua
 *   → Google Chrome ke V8 engine par based
 *
 * - 2010:
 *   → npm (Node Package Manager) introduce hua
 *   → Packages easily install/share karne ka system mila
 *
 * - 2015:
 *   → Node.js Foundation bani
 *   → Development fast aur structured ho gaya
 *
 * - 2019:
 *   → Node.js Foundation + JS Foundation merge hoke OpenJS Foundation bana
 *
 *
 * ! io.js Birth (Important 🔥)
 *
 * - 2014 mein io.js create hua tha
 * - Yeh Node.js ka fork tha (alag version)
 *
 * Reason:
 * - Community Node.js ki slow development se unhappy thi
 * - io.js ne faster updates aur modern features introduce kiye
 *
 *
 * ! Merge of io.js and Node.js
 *
 * - 2015 mein io.js aur Node.js merge ho gaye
 * - Dono ka codebase combine hua
 * - Iske baad Node.js ka development fast ho gaya
 *
 * → Yeh ek important turning point tha Node.js ke growth mein
 *
 *
 * ! Why Node.js Became Popular
 *
 * - Fast execution (V8 engine)
 * - Non-blocking I/O
 * - Event-driven model
 * - JavaScript everywhere (frontend + backend same language)
 *
 *
 * ! Real-World Impact
 *
 * - Companies like Netflix, PayPal, LinkedIn ne Node.js adopt kiya
 * - Scalable aur high-performance apps banana easy ho gaya
 *
 *
 * ! Simple Summary
 *
 * Node.js = Fast + Scalable + JavaScript runtime for backend
 *
 */