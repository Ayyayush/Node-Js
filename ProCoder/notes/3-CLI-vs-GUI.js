/**
 * ! CLI vs GUI
 *
 * CLI (Command Line Interface):
 * - Text-based interface hota hai
 * - User commands type karke system ko instructions deta hai
 * - Fast aur powerful hota hai (developers zyada use karte hain)
 *
 * Example:
 * - Terminal
 * - Command Prompt
 *
 *
 * GUI (Graphical User Interface):
 * - Visual interface hota hai (buttons, icons, windows)
 * - Mouse aur clicks se operate hota hai
 * - Beginner-friendly hota hai
 *
 * Example:
 * - Windows OS
 * - macOS interface
 *
 *
 * ! Top Commands to Know (CLI Basics)
 *
 * - pwd        → current directory ka path show karta hai
 * - ls         → folder ke andar files list karta hai
 * - cd         → directory change karne ke liye
 * - mkdir      → naya folder banane ke liye
 * - rmdir      → folder delete karne ke liye (empty hona chahiye)
 * - touch      → nayi file create karne ke liye
 * - rm         → file delete karne ke liye
 * - clear      → terminal clean karne ke liye
 *
 *
 * ! Important Note
 *
 * - Developers mostly CLI use karte hain kyunki:
 *   → Faster hota hai
 *   → Automation easy hota hai
 *   → Direct control milta hai system par
 *
 */